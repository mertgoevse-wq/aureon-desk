// ── Helpers ─────────────────────────────────────────────────────

function isNpmAvailable(): boolean {
  try {
    execSync('npm --version', { stdio: 'ignore', timeout: 5000 })
    return true
  } catch { return false }
}

function findAvailablePort(basePort: number): number {
  const { execSync } = require('child_process') as typeof import('child_process')
  for (let offset = 0; offset < 100; offset++) {
    const port = basePort + offset
    try {
      // Run a synchronous child process to verify if the port is open.
      // Must set ELECTRON_RUN_AS_NODE to execute as pure Node CLI in Electron env.
      execSync(
        `"${process.execPath}" -e "const s = require('net').createServer(); s.once('error', () => process.exit(1)); s.once('listening', () => { s.close(); process.exit(0); }); s.listen(${port}, '127.0.0.1');"`,
        { stdio: 'ignore', timeout: 2000, env: { ...process.env, ELECTRON_RUN_AS_NODE: '1' } }
      )
      return port
    } catch {
      // Port is busy or check failed, try next offset
    }
  }
  return basePort + Math.floor(Math.random() * 1000)
}

function buildStaticServerScript(sandboxPath: string, port: number): string {
  return `
const http = require('http');
const fs = require('fs');
const path = require('path');
const sandboxRoot = ${JSON.stringify(sandboxPath)};
function getCT(fp) {
  const t={'.html':'text/html','.js':'application/javascript','.css':'text/css','.json':'application/json','.svg':'image/svg+xml','.png':'image/png','.ico':'image/x-icon','.txt':'text/plain'};
  return t[path.extname(fp).toLowerCase()]||'text/plain';
}
const srv = http.createServer((req,res)=>{
  try {
    let fp = path.join(sandboxRoot, req.url==='/'?'index.html':req.url.split('?')[0]);
    if(!fp.startsWith(sandboxRoot)) {res.writeHead(403);res.end('Forbidden');return;}
    if(fs.existsSync(fp)&&fs.statSync(fp).isFile()) {
      res.writeHead(200,{'Content-Type':getCT(fp),'Access-Control-Allow-Origin':'*'});
      res.end(fs.readFileSync(fp));
    } else {
      const idx=path.join(sandboxRoot,'index.html');
      if(fs.existsSync(idx)) {res.writeHead(200,{'Content-Type':'text/html'});res.end(fs.readFileSync(idx));}
      else {res.writeHead(404);res.end('Not found');}
    }
  } catch(e){res.writeHead(500);res.end('Server error');}
});
srv.listen(${port},'127.0.0.1',()=>{console.log('AUREON_PREVIEW_READY:'+${port});});
srv.on('error',(e)=>{console.error('AUREON_PREVIEW_ERROR:'+e.message);process.exit(1);});
`
}

