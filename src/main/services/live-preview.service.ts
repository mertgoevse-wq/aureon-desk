/**
 * LivePreview Service — safe local app preview for generated code.
 */

export interface PreviewSession {
  id: string
  sandboxPath: string
  sandboxUrl: string
  status: 'idle' | 'starting' | 'running' | 'error' | 'stopped'
  port: number
  process: ChildProcess | null
  logs: PreviewLogEntry[]
  templateType: string
  createdAt: string
  error: string | null
}

export interface PreviewLogEntry {
  timestamp: string
  stream: 'stdout' | 'stderr'
  text: string
}

export interface CreateSandboxInput {
  templateType?: string
  port?: number
  style?: string
}

export interface StartGeneratedPreviewInput {
  source: 'studio-build-app' | 'code-demo' | 'manual'
  files?: Record<string, string>
  entryFile?: string
  style?: string
  port?: number
  autoOpenCodeMode?: boolean
  autoFocusPreview?: boolean
}

export interface PreviewStatus {
  id: string | null
  status: 'idle' | 'starting' | 'running' | 'error' | 'stopped'
  sandboxPath: string | null
  url: string | null
  port: number | null
  templateType: string | null
  logs: PreviewLogEntry[]
  error: string | null
}

export interface CodingDemoResult {
  success: boolean
  sandboxId: string
  sandboxPath: string
  url: string | null
  filesWritten: string[]
  previewStatus: PreviewStatus
  error?: string
}

